pub fn aritmetics() {
    let x = 10;
    let y = 5;

    println!("x = {}, y = {}", x, y);
    println!("x + y = {}", x + y);
    println!("x - y = {}", x - y);
    println!("x * y = {}", x * y);
    println!("x / y = {}", x / y);
    println!("x % y = {}", x % y);
}

pub fn relations() {
    let x = 10;
    let y = 5;

    println!("x = {}, y = {}", x, y);
    println!("x == y => {}", x == y);
    println!("x != y => {}", x != y);
    println!("x > y => {}", x > y);
    println!("x < y => {}", x < y);
    println!("x >= y => {}", x >= y);
    println!("x <= y => {}", x <= y);
}

pub fn logics() {
    let x = true;
    let y = false;

    println!("x = {}, y = {}", x, y);
    println!("x && y => {}", x && y);
    println!("x || y => {}", x || y);
    println!("!x => {}", !x);
    println!("!y => {}", !y);
}